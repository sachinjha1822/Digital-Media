        const searchInput = document.getElementById("search-input");
        const suggestionsContainer = document.getElementById("suggestions");
        const searchButton = document.getElementById("search-button");

        // Show suggestions as user types
        searchInput.addEventListener("input", () => {
            const query = searchInput.value.trim().toLowerCase();
            if (!query) {
                suggestionsContainer.style.display = "none";
                suggestionsContainer.innerHTML = "";
                return;
            }

            const matchedProducts = Object.values(products).filter(
                (product) => product.title.toLowerCase().includes(query)
            );

            if (matchedProducts.length === 0) {
                suggestionsContainer.style.display = "none";
                suggestionsContainer.innerHTML = "";
                return;
            }

            suggestionsContainer.innerHTML = matchedProducts
                .map(
                    (product) => `
                        <div class="suggestion-item" data-id="${product.id}" style="display: flex; align-items: center; cursor: pointer; padding: 5px;">
                            <img src="${product.image}" alt="${product.title}" style="width: 40px; height: 40px; object-fit: cover; margin-right: 10px; border-radius: 4px;" />
                            <span>${product.title}</span>
                        </div>`
                )
                .join("");
            suggestionsContainer.style.display = "block";
        });

        // Redirect on clicking a suggestion
        suggestionsContainer.addEventListener("click", (e) => {
            const item = e.target.closest(".suggestion-item");
            if (item) {
                const productId = item.getAttribute("data-id");
                redirectToProduct(productId);
            }
        });

        // Redirect on pressing Enter key in search input
